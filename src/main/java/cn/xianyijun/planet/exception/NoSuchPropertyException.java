package cn.xianyijun.planet.exception;


/**
 * The type No such property exception.
 */
public class NoSuchPropertyException extends RuntimeException {

    /**
     * Instantiates a new No such property exception.
     */
    public NoSuchPropertyException() {
        super();
    }

    /**
     * Instantiates a new No such property exception.
     *
     * @param msg the msg
     */
    public NoSuchPropertyException(String msg) {
        super(msg);
    }
}
